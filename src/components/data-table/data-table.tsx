import {
  type ColumnDef,
  flexRender,
  getCoreRowModel,
  getSortedRowModel,
  useReactTable,
  type SortingState,
  type RowSelectionState,
  type VisibilityState,
  type OnChangeFn,
} from '@tanstack/react-table';
import { useState } from 'react';
import {
  Table,
  TableBody,
  TableCell,
  TableHead,
  TableHeader,
  TableRow,
} from '@/components/ui/table';
import { DataTablePagination } from './data-table-pagination';
import { DataTableColumnCustomizer } from './data-table-column-customizer';
import { EmptyState } from '@/components/shared/empty-state';
import { TableSkeleton } from '@/components/shared/loading-skeleton';
import type { ReactNode } from 'react';

type PaginationData = {
  page: number;
  limit: number;
  total: number;
  totalPages: number;
};

type DataTableProps<TData, TValue> = {
  columns: ColumnDef<TData, TValue>[];
  data: TData[];
  getRowId?: (row: TData) => string;
  pagination?: PaginationData;
  onPageChange?: (page: number) => void;
  onLimitChange?: (limit: number) => void;
  isLoading?: boolean;
  toolbar?: ReactNode | ((columnCustomizer: ReactNode) => ReactNode);
  bulkActions?: ReactNode;
  rowSelection?: RowSelectionState;
  onRowSelectionChange?: (selection: RowSelectionState) => void;
  columnVisibility?: VisibilityState;
  onColumnVisibilityChange?: (visibility: VisibilityState) => void;
  onRowClick?: (row: TData) => void;
  emptyTitle?: string;
  emptyDescription?: string;
  hasActiveFilters?: boolean;
  sorting?: SortingState;
  onSortingChange?: OnChangeFn<SortingState>;
};

export const DataTable = <TData, TValue>({
  columns,
  data,
  getRowId,
  pagination,
  onPageChange,
  onLimitChange,
  isLoading,
  toolbar,
  bulkActions,
  rowSelection: controlledRowSelection,
  onRowSelectionChange,
  columnVisibility: controlledColumnVisibility,
  onColumnVisibilityChange,
  onRowClick,
  emptyTitle,
  emptyDescription,
  hasActiveFilters,
  sorting: controlledSorting,
  onSortingChange,
}: DataTableProps<TData, TValue>) => {
  const [internalSorting, setInternalSorting] = useState<SortingState>([]);
  const [internalRowSelection, setInternalRowSelection] = useState<RowSelectionState>({});
  const [internalColumnVisibility, setInternalColumnVisibility] = useState<VisibilityState>({});

  const isServerSideSort = !!onSortingChange;
  const sorting = controlledSorting ?? internalSorting;
  const rowSelection = controlledRowSelection ?? internalRowSelection;
  const columnVisibility = controlledColumnVisibility ?? internalColumnVisibility;

  const table = useReactTable({
    data,
    columns,
    ...(getRowId && { getRowId }),
    getCoreRowModel: getCoreRowModel(),
    ...(isServerSideSort ? { manualSorting: true } : { getSortedRowModel: getSortedRowModel() }),
    onSortingChange: onSortingChange ?? setInternalSorting,
    onRowSelectionChange: onRowSelectionChange
      ? (updater) => {
          const newSelection = typeof updater === 'function' ? updater(rowSelection) : updater;
          onRowSelectionChange(newSelection);
        }
      : setInternalRowSelection,
    onColumnVisibilityChange: onColumnVisibilityChange
      ? (updater) => {
          const newVisibility =
            typeof updater === 'function' ? updater(columnVisibility) : updater;
          onColumnVisibilityChange(newVisibility);
        }
      : setInternalColumnVisibility,
    state: {
      sorting,
      rowSelection,
      columnVisibility,
    },
    manualPagination: true,
  });

  const selectedCount = Object.keys(rowSelection).length;

  const columnCustomizer = <DataTableColumnCustomizer table={table} />;

  return (
    <div>
      {typeof toolbar === 'function' ? toolbar(columnCustomizer) : toolbar}

      {selectedCount > 0 && bulkActions}

      <div className="rounded-md border">
        {isLoading ? (
          <div className="p-4">
            <TableSkeleton />
          </div>
        ) : data.length === 0 ? (
          hasActiveFilters ? (
            <EmptyState
              title="No results found"
              description="Try adjusting your search or filters."
            />
          ) : (
            <EmptyState title={emptyTitle} description={emptyDescription} />
          )
        ) : (
          <Table>
            <TableHeader>
              {table.getHeaderGroups().map((headerGroup) => (
                <TableRow key={headerGroup.id}>
                  {headerGroup.headers.map((header) => (
                    <TableHead key={header.id}>
                      {header.isPlaceholder
                        ? null
                        : flexRender(header.column.columnDef.header, header.getContext())}
                    </TableHead>
                  ))}
                </TableRow>
              ))}
            </TableHeader>
            <TableBody>
              {table.getRowModel().rows.map((row) => (
                <TableRow
                  key={row.id}
                  data-state={row.getIsSelected() && 'selected'}
                  className={onRowClick ? 'cursor-pointer focus-visible:bg-muted/50 focus-visible:outline-none' : undefined}
                  tabIndex={onRowClick ? 0 : undefined}
                  role={onRowClick ? 'button' : undefined}
                  onClick={onRowClick ? () => onRowClick(row.original) : undefined}
                  onKeyDown={onRowClick ? (e) => {
                    if (e.key === 'Enter' || e.key === ' ') {
                      e.preventDefault();
                      onRowClick(row.original);
                    }
                  } : undefined}
                >
                  {row.getVisibleCells().map((cell) => (
                    <TableCell key={cell.id}>
                      {flexRender(cell.column.columnDef.cell, cell.getContext())}
                    </TableCell>
                  ))}
                </TableRow>
              ))}
            </TableBody>
          </Table>
        )}
      </div>

      {pagination && onPageChange && onLimitChange && (
        <DataTablePagination
          page={pagination.page}
          totalPages={pagination.totalPages}
          total={pagination.total}
          limit={pagination.limit}
          onPageChange={onPageChange}
          onLimitChange={onLimitChange}
          selectedCount={selectedCount}
        />
      )}
    </div>
  );
};
